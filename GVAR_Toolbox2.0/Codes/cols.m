function ncols = cols(x)

%**************************************************************************
% PURPOSE: get the number of columns of a matrix 
%--------------------------------------------------------------------------
% INPUT: 
% - x: m x n matrix 
%--------------------------------------------------------------------------
% OUTPUT: 
% - ncols: n ( # of columns in x) 
%--------------------------------------------------------------------------
% Alessandro Galesi, July 2010
% Centro de Estudios Monetarios y Financieros, Madrid.
% alessandro.galesi@cemfi.edu.es
%**************************************************************************

[nrows ncols]=size(x); %#ok

